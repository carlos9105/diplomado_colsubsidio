function calculadora() {

    var n1 = prompt("Primer número")
    var n2 = prompt("Segundo número")
    var op = prompt("¿Qué operación quieres realizar?, escribe una de las siguientes operciones: +, -, *, /")

    if (op == "+")
        alert("El resultado es: " + (Number(n1) + Number(n2)))

    if (op == "-")
        alert("El resultado es: " + (Number(n1) - Number(n2)))

    if (op == "*")
        alert("El resultado es: " + (Number(n1) * Number(n2)))

    if (op == "/")
        alert("El resultado es: " + (Number(n1) / Number(n2)))

}